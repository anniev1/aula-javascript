function operacaoDuplaVariavel() {

    let numUm = 15;
    let numDois = 67;
    let diferenca = 0;
    let dobroUm = 0;
    let triploDois = 0;
    let somaVar = 0;
    let multiVar = 0;

    alert("O número da primeira variável é " + numUm);

    alert("O número da segunda variável é " + numDois);

    diferenca = numUm - numDois;

    alert("A diferença entre as variáveis é " + diferenca);

    dobroUm = numUm * 2;

    triploDois = numDois * 3;

    somaVar = dobroUm + triploDois;

    alert("O dobro da primeira variável somado ao triplo da segunda é " + somaVar);

    multiVar = numUm + numDois;

    alert("A soma das variáveis é de " + multiVar);
}